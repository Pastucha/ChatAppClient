    private void cmdRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRegisterActionPerformed
      String userName = txtUser.getText().trim();
        String password = String.valueOf(txtPass.getPassword());
        String confirmPassword = String.valueOf(txtRePass.getPassword());
        if (userName.equals("")) {
            txtUser.grabFocus();
        } else if (password.equals("")) {
            txtPass.grabFocus();
        } else if (!password.equals(confirmPassword)) {
            txtPass.grabFocus();
        } else {
            Register data = new Register(userName, password);
            EventHandler.getInstance().getEventLogin().register(data, new EventMessage() {
                @Override
                public void callMessage(Message message) {
                    if (!message.isAction()) {
                        lbError.setText(message.getMessage());
                    } else {
                    EventHandler.getInstance().getEventMain().initChat();
                            
                    }
                }
            });
        }
    }//GEN-LAST:event_cmdRegisterActionPerformed

